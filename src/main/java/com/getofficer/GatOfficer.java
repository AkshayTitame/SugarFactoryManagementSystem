package com.getofficer;

public class GatOfficer {
    private int gatOfficerID;
    private int empID;
    private int areaID;
    private String gatOfficerName;
    private String mobile;
    private String password;

    public int getGatOfficerID() { return gatOfficerID; }
    public void setGatOfficerID(int gatOfficerID) { this.gatOfficerID = gatOfficerID; }

    public int getEmpID() { return empID; }
    public void setEmpID(int empID) { this.empID = empID; }

    public int getAreaID() { return areaID; }
    public void setAreaID(int areaID) { this.areaID = areaID; }

    public String getGatOfficerName() { return gatOfficerName; }
    public void setGatOfficerName(String gatOfficerName) { this.gatOfficerName = gatOfficerName; }

    public String getMobile() { return mobile; }
    public void setMobile(String mobile) { this.mobile = mobile; }

    public String getPassword() { return password; }
    public void setPassword(String password) { this.password = password; }
}
